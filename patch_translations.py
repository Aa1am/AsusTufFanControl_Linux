def patch_file(filepath):
    lang_code = os.path.basename(filepath).split('_')[1].split('.')[0]
    
    print(f"Processing {filepath} (Lang: {lang_code})...")
    
    if lang_code not in TRANSLATIONS:
         print(f"  Warning: No translation dictionary for {lang_code}, skipping.")
         return

    try:
        tree = ET.parse(filepath)
        root = tree.getroot()
        
        updated_count = 0
        
        for context in root.findall('context'):
             for message in context.findall('message'):
                source = message.find('source').text
                translation = message.find('translation')
                
                if source and translation is not None:
                    
                    new_text = None
                    # Exact match
                    if source in TRANSLATIONS[lang_code]:
                        new_text = TRANSLATIONS[lang_code][source]
                    
                    if new_text:
                        # Apply translation if different
                        if translation.text != new_text:
                            translation.text = new_text
                            if 'type' in translation.attrib:
                                del translation.attrib['type'] # Remove "unfinished"
                            updated_count += 1
                            print(f"  Translated: '{source}' -> '{new_text}'")
        
        if updated_count > 0:
            tree.write(filepath, encoding='utf-8', xml_declaration=True)
            print(f"  Saved {filepath}, updated {updated_count} strings.")
        else:
            print(f"  No updates needed for {filepath}.")
            
    except Exception as e:
        print(f"Error processing {filepath}: {e}")

