# New function calls to ensure comprehensive patching
def inject_missing_keys(filepath, missing_keys_map):
    """Injects new context/message blocks if they are missing."""
    try:
        tree = ET.parse(filepath)
        root = tree.getroot()
        updated = False
        lang_code = os.path.basename(filepath).split('_')[1].split('.')[0]
        
        if lang_code not in EXTRA_FINAL:
             return
            
        for context_name, keys in missing_keys_map.items():
             # Find or create context
             context_elem = None
             for ctx in root.findall('context'):
                  name = ctx.find('name')
                  if name is not None and name.text == context_name:
                       context_elem = ctx
                       break
             
             if context_elem is None:
                  context_elem = ET.SubElement(root, 'context')
                  name_elem = ET.SubElement(context_elem, 'name')
                  name_elem.text = context_name
                  updated = True
            
             # Check for each key
             for key in keys:
                  # Check if exists
                  exists = False
                  for msg in context_elem.findall('message'):
                       src = msg.find('source')
                       if src is not None and src.text == key:
                            exists = True
                            # Update if unfinished/empty
                            trans_elem = msg.find('translation')
                            if trans_elem is not None and key in EXTRA_FINAL[lang_code]:
                                 target = EXTRA_FINAL[lang_code][key]
                                 if trans_elem.text != target:
                                      trans_elem.text = target
                                      if 'type' in trans_elem.attrib:
                                           del trans_elem.attrib['type']
                                      updated = True
                                      print(f"  Updated existing key: '{key}' -> '{target}'")
                            break
                  
                  if not exists and key in EXTRA_FINAL[lang_code]:
                       msg = ET.SubElement(context_elem, 'message')
                       src = ET.SubElement(msg, 'source')
                       src.text = key
                       trans = ET.SubElement(msg, 'translation')
                       trans.text = EXTRA_FINAL[lang_code][key]
                       print(f"  Injected new C++ key: '{key}' -> '{trans.text}'")
                       updated = True

        if updated:
             tree.write(filepath, encoding='utf-8', xml_declaration=True)
             print(f"  Saved {filepath}, injected new keys.")

    except Exception as e:
        print(f"Error injecting keys in {filepath}: {e}")

